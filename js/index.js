function openNav() { 
      document.getElementById("mobilescreen").style.height = "100%";
      document.getElementById("hambutton").style.visibility = "hidden";
      document.getElementById("hambuttonclose").style.visibility = "visible";
     
}

function closeNav(){ 
      document.getElementById("mobilescreen").style.height = "0%";
      document.getElementById("hambutton").style.visibility = "visible";
      document.getElementById("hambuttonclose").style.visibility = "hidden";
      
}     

